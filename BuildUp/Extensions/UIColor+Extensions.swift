extension UIColor {
    func isLight() -> Bool {
        // algorithm from: http://www.w3.org/WAI/ER/WD-AERT/#color-contrast
        let components = self.cgColor.components
        let comp1 = ((components?[0] ?? 0) * 299)
        let comp2 = ((components?[1] ?? 0) * 587)
        let comp3 = ((components?[2] ?? 0) * 114)
        
        let brightness = (comp1 + comp2 + comp3) / (1000)
        
        if brightness < 0.5 {
            return false
        } else {
            return true
        }
    }
    
}
